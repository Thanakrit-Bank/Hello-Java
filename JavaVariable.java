public class JavaVariable {
	public static void main (String[] args) {
	String Handsome = "Tua";
	System.out.println(Handsome);
	int number = 10;
	System.out.println(number);
	float number2 = 5.2f;
	System.out.println(number2);
	boolean TuaHandsome = true;
	System.out.println(TuaHandsome);
	char WhatmyGrade = 'A';
	System.out.println(WhatmyGrade);
}
}

