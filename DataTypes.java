public class DataTypes {
        public static void main (String[] args) {
        	byte Num = 127;//byte can store from -128 to 127 
		System.out.println(Num);
		short Num2 = 30000;//short can store from -32768 to 32767
		System.out.println(Num2);
		int num3 = 2541321012; // int can store from -2147483648 to 2147483647
		System.out.println(num3);
		double num4 = 1.999d;// double can store fractional number from 1.7e-308 to 1.7e+308.
		System.out.println(num4);
}
}
